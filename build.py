# ---------------------------------------------------------------------------
# 3. Run PyInstaller
# ---------------------------------------------------------------------------
def run_pyinstaller():
    print("[..] Running PyInstaller — this takes 1-3 minutes...")
    cmd = [
        sys.executable, "-m", "PyInstaller",
        "--clean",
        "--noconfirm",
        str(SPEC_FILE),
    ]
    result = subprocess.run(cmd, cwd=str(HERE))
    if result.returncode != 0:
        print("\n[ERROR] PyInstaller failed — see output above.")
        sys.exit(1)
    print("[OK] Build complete.")


# ---------------------------------------------------------------------------
# 4. Post-build: copy config and write a launcher batch file
# ---------------------------------------------------------------------------
def post_build():
    DIST_DIR.mkdir(parents=True, exist_ok=True)

    # config.ini must sit NEXT TO the exe (in POIScanner/, NOT in _internal/).
    # PyInstaller puts all Python modules inside _internal/ but the exe itself
    # lives in POIScanner/ — that is where os.chdir() lands at runtime.
    config_dest = DIST_DIR / "config.ini"
    if CONFIG.exists():
        shutil.copy2(CONFIG, config_dest)
        print(f"[OK] config.ini -> {config_dest}")
        print(f"     (next to the exe, NOT inside _internal/)")
    else:
        print(f"[WARN] config.ini not found in project root.")
        print(f"       Copy it manually to: {config_dest}")

    # Write a .bat launcher for convenience (Windows)
    bat = DIST_DIR / "Run_POIScanner.bat"
    bat.write_text(
        '@echo off\n'
        'cd /d "%~dp0"\n'
        'POIScanner.exe\n'
        'if %ERRORLEVEL% NEQ 0 (\n'
        '    echo.\n'
        '    echo Scanner exited with error %ERRORLEVEL%\n'
        '    echo Check startup_error.log in this folder.\n'
        '    pause\n'
        ')\n',
        encoding="utf-8",
    )
    print(f"[OK] Run_POIScanner.bat written (double-click this to start)")


