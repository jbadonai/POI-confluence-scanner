"""
build.py — one-click PyInstaller build for POI Scanner.

Usage
-----
  Normal build:
    python build.py

  Debug build (keeps console open on crash, verbose imports):
    python build.py --debug

Output
------
  dist/POIScanner/
    POIScanner.exe   <- the executable
    config.ini       <- copy of your config (edit credentials before running)
    startup_error.log  <- written automatically if the exe crashes at startup
    poi_scanner.log    <- written by the scanner during normal operation
"""

